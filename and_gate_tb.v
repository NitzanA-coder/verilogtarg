`timescale 1ns / 1ps // הגדרת יחידת זמן: כל יחידה היא 1 ננו-שנייה

module and_gate_tb; // ל-Testbench אין כניסות ויציאות בכלל!

    // 1. הגדרת סיגנלים לחיבור לרכיב הנבדק
    reg  t_a;   // כניסות מוגדרות כ-reg כי נשנה אותן בתוך בלוק initial
    reg  t_b;
    wire t_c;   // יציאות מוגדרות כ-wire

    // 2. חיבור הרכיב האמיתי (UUT - Unit Under Test)
    and_gate uut (
        .a(t_a),
        .b(t_b),
        .c(t_c)
    );

    // 3. יצירת תרחישי הבדיקה (רצים פעם אחת מהתחלה עד הסוף)
    initial begin
        // מצב 1: שתי הכניסות ב-0
        t_a = 0; t_b = 0;
        #10; // המתן 10 ננו-שניות
        
        // מצב 2
        t_a = 0; t_b = 1;
        #10;
        
        // מצב 3
        t_a = 1; t_b = 0;
        #10;
        
        // מצב 4: שתיהן ב-1 (התוצאה אמורה להשתנות ל-1)
        t_a = 1; t_b = 1;
        #10;
        
        $finish; // סיום הסימולציה
    end

endmodule